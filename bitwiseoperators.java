public class bitwiseoperators 
{
    public static void main(String args[])
    {
        int a = 5;
        int b = 7;

        //bitwise AND operation
        // 0101 & 0111 = 0101 = 5
        System.out.println("a & b = " + (a & b));

        //bitwise OR operation
        // 0101 & 0111 = 0101 = 7
        System.out.println("a | b = " + (a | b));
    }
}
